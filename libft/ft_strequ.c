int		ft_strequ(char const *str1, char const *str2)
{
	if (!str1 || !str2)
		return (0);
	while (*str1 || *str2)
	{
		if (*str1 != *str2)
			return (0);
		str1++;
		str2++;
	}
	return (1);
}
